#include "pushswap.h"

void	print(t_stack a)
{
	int	i;

	i = a.size - 1;
	if (a.size > 0)
	{
		while (i >= 0)
		{
			ft_putnbr_fd((a.arr)[i], 1);
			write(1, "\n", 1);
			i--;
		}
		ft_putstr_fd("\n", 1);
	}
}
